// There are many methods in Arrays

var myArray = [10,20]

myArray.push(14); // add element at last

console.log(myArray);

myArray.pop(); // delete last element

console.log(myArray);

myArray.shift(50);// Remove at first position

console.log(myArray);

myArray.unshift(40); // inset element at first position

console.log(myArray);
